let aj:number=10, ak:number=20;

console.log(aj+ak);
console.log(aj-ak);
console.log(aj*ak);
console.log(aj/ak); //division operator gives the quotient when aj is divided by ak
console.log(aj%ak); //modulus operator gives the remainder when aj is divided by ak
console.log(aj**ak); //exponentiation operator gives the result of aj raised to the power of ak